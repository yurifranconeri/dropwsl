"""Streamlit Showcase -- demonstration of all available components."""

from datetime import date, datetime, time

import numpy as np
import pandas as pd
import streamlit as st

st.set_page_config(page_title="{{PROJECT_NAME}}", page_icon="🚀", layout="wide")

# ---- Sidebar (global settings + input widgets) ----
st.sidebar.header("⚙️ Settings")
user_name = st.sidebar.text_input("Your name", value="World")
palette = st.sidebar.radio("Color palette", ["Default", "Ocean", "Neon"])
CHART_COLORS: dict[str, list[str] | None] = {
    "Default": None,
    "Ocean": ["#264653", "#2a9d8f", "#e76f51"],
    "Neon": ["#ff006e", "#8338ec", "#3a86ff"],
}
chart_colors: list[str] | None = CHART_COLORS[palette]
show_code = st.sidebar.toggle("Show code examples", value=False)
st.sidebar.divider()
st.sidebar.caption("Showcase generated by dropwsl")

# ---- Header ----
st.title("{{PROJECT_NAME}} 🚀")
st.markdown(
    "**Complete showcase** of Streamlit components. "
    "Explore each tab to see working examples of all widgets, "
    "charts, layouts and available elements."
)

# ---- Main tabs ----
tab_widgets, tab_data, tab_charts, tab_layout, tab_chat, tab_status, tab_forms = st.tabs(
    ["🎛️ Widgets", "📊 Data", "📈 Charts", "📐 Layout", "💬 Chat", "📢 Status", "📝 Forms"]
)

# ================================================================
# TAB 1: WIDGETS -- all native input widgets
# ================================================================
with tab_widgets:
    st.header("Input Widgets")

    col1, col2, col3 = st.columns(3)

    with col1:
        st.subheader("Text")
        text_val = st.text_input("Text input", placeholder="Type something...")
        area_val = st.text_area("Text area", placeholder="Long text here...")
        if text_val:
            st.write(f"You typed: {text_val}")

    with col2:
        st.subheader("Numbers and Dates")
        num = st.number_input("Number", min_value=0, max_value=100, value=42)
        slider_val = st.slider("Slider", 0, 100, 50)
        range_val = st.slider("Range slider", 0.0, 100.0, (25.0, 75.0))
        date_val = st.date_input("Date", value=date.today())
        time_val = st.time_input("Time", value=time(9, 0))

    with col3:
        st.subheader("Selection")
        select = st.selectbox("Selectbox", ["Python", "JavaScript", "Go", "Rust"])
        multi = st.multiselect(
            "Multiselect", ["FastAPI", "Django", "Flask", "Streamlit"], default=["FastAPI"]
        )
        radio_val = st.radio("Radio", ["Option A", "Option B", "Option C"], horizontal=True)

    st.divider()

    col4, col5, col6 = st.columns(3)

    with col4:
        st.subheader("Toggles and Checks")
        check = st.checkbox("Checkbox", value=True)
        toggle_val = st.toggle("Toggle", value=False)
        color = st.color_picker("Color", value="#FF4B4B")
        st.write(f"Selected color: {color}")

    with col5:
        st.subheader("Select Slider")
        size = st.select_slider("Size", options=["XS", "S", "M", "L", "XL"], value="M")
        st.write(f"Selected: {size}")

    with col6:
        st.subheader("Upload and Feedback")
        uploaded = st.file_uploader("File upload", type=["csv", "txt", "json"])
        if uploaded:
            st.success(f"File received: {uploaded.name} ({uploaded.size} bytes)")
        feedback_val = st.feedback("stars")
        if feedback_val is not None:
            st.write(f"Feedback: {'⭐' * (feedback_val + 1)}")

    if show_code:
        with st.expander("📝 Source code for this section"):
            st.code(
                'text_val = st.text_input("Text input", placeholder="...")\n'
                'slider_val = st.slider("Slider", 0, 100, 50)\n'
                'select = st.selectbox("Selectbox", ["Python", "JavaScript"])\n'
                'uploaded = st.file_uploader("Upload", type=["csv"])',
                language="python",
            )

# ================================================================
# TAB 2: DATA -- dataframe, data_editor, metric, json, table
# ================================================================
with tab_data:
    st.header("Data Elements")

    # Metrics
    st.subheader("Metrics")
    m1, m2, m3, m4 = st.columns(4)
    m1.metric("Revenue", "$12.4k", "+8.2%")
    m2.metric("Users", "1,234", "+12%")
    m3.metric("Latency", "42ms", "-5ms")
    m4.metric("Uptime", "99.9%", "+0.1%")

    st.divider()

    # Interactive DataFrame vs static table
    df = pd.DataFrame(
        {
            "Name": ["Alice", "Bob", "Carol", "David", "Eva"],
            "Age": [30, 25, 35, 28, 32],
            "Salary": [8500, 6200, 9800, 7100, 8900],
            "Active": [True, False, True, True, False],
            "Start Date": [
                date(2020, 1, 15),
                date(2021, 3, 22),
                date(2019, 7, 10),
                date(2022, 11, 5),
                date(2023, 2, 28),
            ],
        }
    )

    col_df, col_table = st.columns(2)

    with col_df:
        st.subheader("Interactive DataFrame")
        st.dataframe(df, width="stretch", hide_index=True)

    with col_table:
        st.subheader("Static table")
        st.table(df.head(3))

    st.divider()

    # Data editor
    st.subheader("Data Editor (editable)")
    edited_df = st.data_editor(
        df,
        num_rows="dynamic",
        width="stretch",
        column_config={
            "Salary": st.column_config.NumberColumn("Salary ($)", format="$ %d"),
            "Active": st.column_config.CheckboxColumn("Active?"),
            "Start Date": st.column_config.DateColumn("Start Date", format="YYYY-MM-DD"),
        },
    )

    st.divider()

    # JSON
    st.subheader("JSON viewer")
    st.json(
        {
            "project": "{{PROJECT_NAME}}",
            "version": "0.1.0",
            "config": {"debug": True, "workers": 4, "tags": ["demo", "showcase"]},
        }
    )

    if show_code:
        with st.expander("📝 Source code for this section"):
            st.code(
                'st.metric("Revenue", "$12.4k", "+8.2%")\n'
                'st.dataframe(df, use_container_width=True)\n'
                "edited_df = st.data_editor(df, num_rows=\"dynamic\")\n"
                'st.json({"key": "value"})',
                language="python",
            )

# ================================================================
