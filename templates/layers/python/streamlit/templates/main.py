    if show_code:
        with st.expander("📝 Source code for this section"):
            st.code(
                "chart_data = pd.DataFrame(np.random.randn(30, 3), columns=[...])\n"
                "st.line_chart(chart_data)\n"
                "st.bar_chart(chart_data)\n"
                'st.scatter_chart(scatter_data, x="x", y="y", size="size")\n'
                "st.map(map_data)",
                language="python",
            )

